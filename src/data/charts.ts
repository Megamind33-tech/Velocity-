function notesSpread(count: number, dur: number, base = 0.5): SongNote[] {
    const out: SongNote[] = [];
    for (let i = 0; i < count; i++) {
        const t = 0.7 + (i * dur) / Math.max(1, count - 1);
        const wobble = Math.sin(i * 1.7) * 0.18;
        out.push({ time: t, pitch: Math.max(0.15, Math.min(0.85, base + wobble)) });
    }
    return out;
}

function notesWave(count: number, span: number): SongNote[] {
    const out: SongNote[] = [];
    for (let i = 0; i < count; i++) {
        const t = 0.6 + i * (span / count);
        const p = 0.5 + 0.35 * Math.sin((i / count) * Math.PI * 2);
        out.push({ time: t, pitch: p });
    }
    return out;
}

